# Renders a minimal ai-dba-collector.yaml into the E2E runtime directory.
# The collector and the server share the same datastore, the same secret
# file, and the same DSN parameters; the collector is only invoked to
# apply the operational schema (cluster_groups, alerts, blackouts, etc.)
# that the server's HTTP handlers read from. See apply-collector-schema.sh.
#
set -euo pipefail

: "${E2E_RUN_DIR:?must be set}"
: "${E2E_DB_HOST:?must be set}"
: "${E2E_DB_PORT:?must be set}"
: "${E2E_DB_NAME:?must be set}"
: "${E2E_DB_USER:?must be set}"
: "${E2E_DB_PASSWORD:?must be set}"

CONFIG_PATH="${E2E_RUN_DIR}/ai-dba-collector.yaml"
SECRET_PATH="${E2E_RUN_DIR}/ai-dba-server.secret"

if [ ! -f "${SECRET_PATH}" ]; then
    echo "ERROR: server secret file not found at ${SECRET_PATH}" >&2
    echo "       run render-config.sh first" >&2
    exit 1
fi

mkdir -p "${E2E_RUN_DIR}"

cat > "${CONFIG_PATH}" <<YAML
# Auto-generated by render-collector-config.sh. Do not edit by hand.
datastore:
  host: "${E2E_DB_HOST}"
  port: ${E2E_DB_PORT}
  database: "${E2E_DB_NAME}"
  username: "${E2E_DB_USER}"
  password: "${E2E_DB_PASSWORD}"
  sslmode: "disable"

pool:
  datastore_max_connections: 4
  datastore_max_idle_seconds: 60
  datastore_max_wait_seconds: 30
  max_connections_per_server: 2
  monitored_max_idle_seconds: 60
  monitored_max_wait_seconds: 30

secret_file: "${SECRET_PATH}"
YAML

echo "Rendered collector config: ${CONFIG_PATH}"
