# Renders a minimal ai-dba-server.yaml + secret file into the
# E2E runtime directory. All credentials and ports come from env vars
# so the same script serves CI and local runs.
#
set -euo pipefail

: "${E2E_RUN_DIR:?must be set}"
: "${E2E_DB_HOST:?must be set}"
: "${E2E_DB_PORT:?must be set}"
: "${E2E_DB_NAME:?must be set}"
: "${E2E_DB_USER:?must be set}"
: "${E2E_DB_PASSWORD:?must be set}"
: "${E2E_SERVER_ADDR:?must be set}"

CONFIG_PATH="${E2E_RUN_DIR}/ai-dba-server.yaml"
SECRET_PATH="${E2E_RUN_DIR}/ai-dba-server.secret"
DATA_DIR="${E2E_RUN_DIR}/data"

mkdir -p "${E2E_RUN_DIR}" "${DATA_DIR}"

# Generate a random encryption secret if absent.
if [ ! -f "${SECRET_PATH}" ]; then
    openssl rand -hex 32 > "${SECRET_PATH}"
    chmod 600 "${SECRET_PATH}"
fi

cat > "${CONFIG_PATH}" <<YAML
# Auto-generated by render-config.sh. Do not edit by hand.
http:
  address: "${E2E_SERVER_ADDR}"
  tls:
    enabled: false
  auth:
    enabled: true
    max_failed_attempts_before_lockout: 100
    rate_limit_max_attempts: 1000

connection_security:
  allow_internal_networks: true

database:
  host: "${E2E_DB_HOST}"
  port: ${E2E_DB_PORT}
  database: "${E2E_DB_NAME}"
  user: "${E2E_DB_USER}"
  password: "${E2E_DB_PASSWORD}"
  sslmode: "disable"
  pool_max_conns: 4

embedding:
  enabled: false

llm:
  provider: "ollama"
  model: "smoke-test-noop"

builtins:
  tools:
    query_database: true
    get_schema_info: true
    similarity_search: false
    execute_explain: true
    count_rows: true
    list_probes: true
    describe_probe: true
    query_metrics: true
    list_connections: true
    query_datastore: true
    read_resource: true
    get_alert_rules: true
    get_alert_history: true
    get_blackouts: true
    get_metric_baselines: true
    generate_embedding: false
    search_knowledgebase: false
  resources:
    system_info: true
    connection_info: true

secret_file: "${SECRET_PATH}"
data_dir: "${DATA_DIR}"
YAML

echo "Rendered config: ${CONFIG_PATH}"
echo "Secret file: ${SECRET_PATH}"
echo "Data dir: ${DATA_DIR}"
